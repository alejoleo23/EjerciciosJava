    private void btnCalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCalcularActionPerformed
        // TODO add your handling code here:
        int numeroA, numeroB;
        numeroA = Integer.parseInt(txtNumeroA.getText());
        numeroB = Integer.parseInt(txtNumeroB.getText());
        Divisibilidad divisibilidad;
        divisibilidad = new Divisibilidad(numeroA, numeroB);
        if(divisibilidad.EsDivisible())
            txtRespuesta.setText(numeroA + " es divisible por " + numeroB);
        else
            txtRespuesta.setText(numeroA + " no es dibisible por " + numeroB);
    }//GEN-LAST:event_btnCalcularActionPerformed

